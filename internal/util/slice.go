package util

// StringToAnySlice convert a string slice to an any slice in the format '[]any{[]any{}}'. That is needed for SQL queries, because we need it in the form '("11111111-1111-1111-1111-111111111111"),("22222222-2222-2222-2222-222222222222")' and not in the form '("11111111-1111-1111-1111-111111111111","22222222-2222-2222-2222-222222222222")'.
func StringToAnySlice(i []string) []any {
	var r = []any{}

	for j := range i {
		r = append(r, []any{i[j]})
	}

	return r
}
