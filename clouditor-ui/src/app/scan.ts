import { Asset } from './asset';

export class DiscoveryResult {
  timestamp: Date;
  discoveredAssets: Asset[] = [];
}

export class Scan {
  lastResult: DiscoveryResult;
  group: string;
  assetType: string;
  service: string;
  enabled: any;
  isDiscovering: boolean;
  _id: string;

  static fromJSON(json: any): Scan {
    const scan = Object.assign(new Scan(), json);
    if (json.lastResult != null) {
      scan.lastResult = Object.assign(new DiscoveryResult(), json.lastResult);
      scan.lastResult.discoveredAssets = Object.values(json.lastResult.discoveredAssets).map((x: any) => Object.assign(new Asset(), x));
    }

    return scan;
  }
}
