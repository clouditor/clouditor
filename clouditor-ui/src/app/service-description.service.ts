import { Injectable } from '@angular/core';
import { HttpClient } from '@angular/common/http';
import { Observable } from 'rxjs';
import { map } from 'rxjs/operators';

import { ConfigService } from './config.service';
import { ServiceDescription } from './service-description';

@Injectable()
export class ServiceDescriptionService {
  constructor(private http: HttpClient,
    private config: ConfigService) { }

  getServiceDescription(): Observable<ServiceDescription> {
    // we need to make a copy, to actually have a real ServiceDescription object
    return this.http
      .get<ServiceDescription>(this.config.get().apiUrl + '/service/')
      .pipe(
        map(response => new ServiceDescription(response.components, response.id))
      );
  }
}
