package io.clouditor.assurance;

import static org.junit.jupiter.api.Assertions.assertFalse;
import static org.junit.jupiter.api.Assertions.assertNotNull;

import io.clouditor.AbstractEngineUnitTest;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.Disabled;

class CertificationImporterTest extends AbstractEngineUnitTest {

  @Test
  @Disabled
  void testImporters() {
    var service = this.engine.getService(CertificationService.class);

    service.loadImporters();

    for (var certificationId : service.getImporters().keySet()) {
      var certification = service.load(certificationId);

      assertNotNull(certification);

      assertFalse(certification.getControls().isEmpty());
    }
  }
}
