package io.clouditor.assurance.ccl;

import java.util.List;
import java.util.Map;

public class EmptyExpression extends Expression {

  private String field;

  public String getField() {
    return field;
  }

  public void setField(String field) {
    this.field = field;
  }

  @Override
  public boolean evaluate(Map properties) {
    var fieldValue = getValueFromField(properties, this.field);

    // always return true, if the value is null
    if (fieldValue == null) {
      return true;
    }

    if (fieldValue instanceof List) {
      return ((List) fieldValue).isEmpty();
    }

    if (fieldValue instanceof String) {
      return ((String) fieldValue).isEmpty();
    }

    // TODO: what to do if fieldValue is not an array or string
    return false;
  }
}
