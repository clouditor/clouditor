package io.clouditor.discovery;

import java.util.HashMap;
import java.util.Objects;
import java.util.Set;
import java.util.stream.Collectors;
import org.jvnet.hk2.annotations.Service;

@Service
public class AssetService {

  private final HashMap<String, Asset> assets = new HashMap<>();

  public void update(Asset asset) {
    this.assets.put(asset.getId(), asset);
  }

  public Set<Asset> getAssetsWithType(String assetType) {
    return this.assets.values().stream()
        .filter(asset -> Objects.equals(asset.getType(), assetType))
        .collect(Collectors.toSet());
  }

  public Asset get(String id) {
    return this.assets.get(id);
  }
}
