package io.clouditor.discovery.aws;

import java.util.function.Function;
import software.amazon.awssdk.regions.Region;
import software.amazon.awssdk.services.iam.IamClient;
import software.amazon.awssdk.services.iam.IamClientBuilder;
import software.amazon.awssdk.utils.builder.ToCopyableBuilder;

abstract class AwsIamScanner<T extends ToCopyableBuilder>
    extends AwsScanner<IamClient, IamClientBuilder, T> {

  AwsIamScanner(Function<T, String> idGenerator, Function<T, String> nameGenerator) {
    super(() -> IamClient.builder().region(Region.AWS_GLOBAL), idGenerator, nameGenerator);
  }
}
