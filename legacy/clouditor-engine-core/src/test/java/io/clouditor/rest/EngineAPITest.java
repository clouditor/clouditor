package io.clouditor.rest;

import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertNotEquals;
import static org.junit.jupiter.api.Assertions.assertNull;

import io.clouditor.AbstractEngineUnitTest;
import org.junit.jupiter.api.Test;

class EngineAPITest extends AbstractEngineUnitTest {

  @Test
  void testStart() {
    // port 0 is set through AbstractEngineUnitTest
    assertEquals(0, this.engine.getAPIPort());

    this.engine.startAPI();

    assertNotEquals(0, this.engine.getAPIPort());

    this.engine.stopAPI();
  }

  @Test
  void testStopIfNotRunning() {
    // assert, that the service is not running
    assertNull(this.engine.getAPI());

    // stop via Clouditor
    this.engine.stopAPI();

    // stop directly
    var api = new EngineAPI(this.engine);
    api.stop();
  }
}
